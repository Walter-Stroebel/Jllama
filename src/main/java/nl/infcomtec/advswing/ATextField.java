public class ATextField extends JTextField {

    public ATextField() {
    }

    public ATextField(String text) {
        super(text);
    }

    public ATextField(int columns) {
        super(columns);
    }

    public ATextField(String text, int columns) {
        super(text, columns);
    }

}
